    public void tampilkanData() {
        String[] kolom = {"ID Barang Keluar", "Nama Barang", "QTY", "Tanggal", "Keterangan"};
        ArrayList<brgrusak> list = new brgrusak().getAll();
        Object rowData[] = new Object[5];
        
        tblBr.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for(brgrusak bk:list) {
            rowData[0] = bk.getIdbr();
            rowData[1] = bk.getIdbarang().getNamabarang();
            rowData[2] = bk.getQty();
            rowData[3] = bk.getTanggal();
            rowData[4] = bk.getKet();
            
            ((DefaultTableModel) tblBr.getModel()).addRow(rowData);
        }
    }
    
    public String tampilkanTanggal(){
        DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String tgl = dateFormat.format(Calendar.getInstance().getTime());
        tanggal.setText(tgl);
        return tgl;
    }

