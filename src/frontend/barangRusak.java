    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        barang brg = new barang().getByName(namaBarang.getText());
        if(brg.getIdbarang() == 0){
            JOptionPane.showMessageDialog(null, "Barang tidak ditemukan");
            return;
        }
        brgrusak br = new brgrusak();
        br.setIdbr(Integer.parseInt(idBr.getText()));
        br.setIdbarang(brg);
        br.setQty(Integer.parseInt(qty.getText()));
        br.setTanggal(tanggal.getText());
        br.setKet(ket.getText());
        br.save();
        
        System.out.println(brg.getIdbarang());
        brg.setQty(brg.getQty() - Integer.parseInt(qty.getText()));
        brg.save();
        idBr.setText(Integer.toString(br.getIdbr()));
        tampilkanData();
    }//GEN-LAST:event_btnSimpanActionPerformed

