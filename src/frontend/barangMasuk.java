    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        barang brg = new barang().getByName(namaBarang.getText());
        System.out.println(brg.getIdbarang());
        
//        brg.setQty(Integer.parseInt(qty.getText()));
        if(brg.getIdbarang() == 0){
            JOptionPane.showMessageDialog(null, "Barang tidak ditemukan");
            return;
        }
        brgmasuk bm = new brgmasuk();
        bm.setIdbm(Integer.parseInt(idBm.getText()));
        bm.setIdbarang(brg);
        bm.setQty(Integer.parseInt(qty.getText()));
        bm.setTanggal(tanggal.getText());
        bm.save();
        idBm.setText(Integer.toString(bm.getIdbm()));

        brg.setQty(brg.getQty() + Integer.parseInt(qty.getText()));
        brg.save();
        tampilkanData();
    }//GEN-LAST:event_btnSimpanActionPerformed

