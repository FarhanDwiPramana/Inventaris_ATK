    public void tampilkanData() {
        String[] kolom = {"ID Barang Masuk", "Nama Barang", "QTY", "Tanggal"};
        ArrayList<brgmasuk> list = new brgmasuk().getAll();
        Object rowData[] = new Object[4];
        
        tblBm.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for(brgmasuk bm:list) {
            rowData[0] = bm.getIdbm();
            rowData[1] = bm.getIdbarang().getNamabarang();
            rowData[2] = bm.getQty();
            rowData[3] = bm.getTanggal();
            
            ((DefaultTableModel) tblBm.getModel()).addRow(rowData);
        }
    }
    
    public String tampilkanTanggal(){
        DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String tgl = dateFormat.format(Calendar.getInstance().getTime());
        tanggal.setText(tgl);
        return tgl;
    }
    

