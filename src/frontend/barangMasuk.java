    private void tblBmMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBmMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblBm.getModel();
        int row = tblBm.getSelectedRow();

        brgmasuk bm = new brgmasuk().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        idBm.setText(model.getValueAt(row, 0).toString());
        namaBarang.setText(Integer.toString(bm.getIdbarang().getIdbarang()));
        qty.setText(model.getValueAt(row, 2).toString());
        tanggal.setText(model.getValueAt(row, 3).toString());
        
        outBarang.setText(bm.getIdbarang().getNamabarang());
    }//GEN-LAST:event_tblBmMouseClicked

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblBm.getModel();
        int row = tblBm.getSelectedRow();
        
        brgmasuk bm = new brgmasuk();
        bm.setIdbm(Integer.parseInt(model.getValueAt(row, 0).toString()));
        bm.delete();
        kosongkanForm();
        tampilkanData();
    }//GEN-LAST:event_btnHapusActionPerformed

